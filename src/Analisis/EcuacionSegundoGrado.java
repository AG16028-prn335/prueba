    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        if (!txtCoeCuadrado.getText().equals("")){
          if (!txtCoeLineal.getText().equals("")){
            if (!txtIndependiente.getText().equals("")){
               lblvalor.setVisible(false);
               lblfuncion.setText("Cuantos decimales?");
               txtDecimal.setVisible(true);
               btnNo.setVisible(false);
               btnCalcular.setVisible(true);
               lbla.setVisible(false);
               lblb.setVisible(false);
               lblc.setVisible(false);
                btnNo.setVisible(true);
                btnNext.setVisible(false);
                txtCoeCuadrado.setVisible(false);
                txtCoeLineal.setVisible(false);
                txtIndependiente.setVisible(false);
           }else{
                JOptionPane.showMessageDialog(null, "ingresa un valor en \nel termino independiente");
                txtIndependiente.setRequestFocusEnabled(true);
            }
          }else{
              JOptionPane.showMessageDialog(null, "ingresa un valor en \nel coeficiente lineal");
                txtIndependiente.setRequestFocusEnabled(true);
          }   
        }else{
            JOptionPane.showMessageDialog(null, "ingresa un valor en \nel coeficiente cuadratico");
                txtIndependiente.setRequestFocusEnabled(true);
        }
    }//GEN-LAST:event_btnNextActionPerformed

